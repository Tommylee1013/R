# 산술 연산자
print(2 + 2) # 덧셈
print(2 - 1) # 뺄셈
print(2 * 2) # 곱셈
print(4 / 2) # 나눗셈
print(5 %/% 2) # 몫
print(5 %% 2) # 나머지
print((3 + 2) ^ 3) # 거듭제곱

# Variables
